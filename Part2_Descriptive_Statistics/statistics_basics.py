# Basic Descriptive Statistics

import statistics as st

#Simple data
data = [1, 2, 3, 3]

#Finding the mean
mean = st.mean(data)

#Finding the median 
median = st.median(data)

#Finding the mode 
mode = st.mode(data)

#Finding the variance  
variance = st.variance(data)

#Finding the standard deviation
standard_deviation = st.stdev(data)